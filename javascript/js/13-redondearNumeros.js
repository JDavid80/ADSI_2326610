/* Redondear hacia arriba con método ceil*/
var numero4 = 7.1;
var numero4Reondeado = Math.ceil(numero4);
console.log(numero4Reondeado);

/* Redondear hacia abajo con método floor */
var numero5 = 7.9;
var numero5Redondeado = Math.floor(numero5);
console.log(numero5Redondeado);

